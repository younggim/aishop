export function getProductById(id: string): Product | undefined {
  return products.find((p) => p.id === id);
}

export function getProductsByCategory(categoryId: string): Product[] {
  return products.filter((p) => p.categoryId === categoryId);
}

export function getProductsByTag(tag: string): Product[] {
  return products.filter((p) => p.tags.includes(tag as Product["tags"][number]));
}

export function getFeaturedProducts(limit = 8): Product[] {
  return products.filter((p) => p.tags.includes("mdPick")).slice(0, limit);
}

export function getDiscountedProducts(limit = 6): Product[] {
  return products
    .filter((p) => p.discountRate && p.discountRate > 0)
    .sort((a, b) => (b.discountRate ?? 0) - (a.discountRate ?? 0))
    .slice(0, limit);
}

export function getNewProducts(limit = 8): Product[] {
  return products.filter((p) => p.tags.includes("newArrival")).slice(0, limit);
}

export function getSpeedDeliveryProducts(limit = 6): Product[] {
  return products.filter((p) => p.tags.includes("speedDelivery")).slice(0, limit);
}

export function searchProducts(query: string): Product[] {
  const q = query.trim().toLowerCase();
  if (!q) return [];
  return products.filter(
    (p) =>
      p.name.toLowerCase().includes(q) ||
      p.brand.toLowerCase().includes(q) ||
      p.description.toLowerCase().includes(q)
  );
}

export function discountedPrice(product: Product): number {
  if (!product.discountRate) return product.price;
  return Math.round(product.price * (1 - product.discountRate / 100));
}
